.MODEL SMALL
.STACK 100h
.DATA
   MSG DB 13,10,'NHAP SO THAP PHAN: $' 
   MSG1 DB 13,10,'XUAT SO THAP PHAN: $'
   TONG DW ?   
   SO DB ?
.CODE
   MOV AX,@DATA
   MOV DS,AX

; NHAP MOT SO THAP PHAN
RESET:   
   MOV AH,9h
   LEA DX,MSG
   INT 21h
   
   MOV TONG,0

INPUT:   
   MOV AH,1
   INT 21h 
   
   CMP AL,0Dh
   JE  BREAK
   
   CMP AL,'0'
   JB RESET
   CMP AL,'9'
   JA RESET
   
   SUB AL,30h
   
   MOV CL,AL 
   XOR CH,CH
   
   MOV AX,TONG
   MOV DX,10
   MUL DX ; => AX = AX * DX=TONG *10
   ADD AX,CX 
   MOV TONG,AX
   JMP INPUT      
   
KETTHUC:
   MOV AH,4Ch
   INT 21h
END